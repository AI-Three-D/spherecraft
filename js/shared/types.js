// ─── Tile Material Categories ─────────────────────────────────────────
// Groups tile type IDs by surface material for category-aware splat
// accumulation. Each category covers all subtypes and variants of a
// material (e.g. GRASS includes short, medium, tall, meadow, flower).
// The splat compute shader accumulates kernel weights per category so
// biome boundaries always produce a cross-category blend rather than
// collapsing to two variants of the same material.

export const TILE_CATEGORIES = [
    { id: 0,  name: 'WATER',    ranges: [[0, 3]] },
    { id: 1,  name: 'GRASS',    ranges: [[10, 29]] },
    { id: 2,  name: 'SAND',     ranges: [[30, 41]] },
    { id: 3,  name: 'ROCK',     ranges: [[42, 53]] },
    { id: 4,  name: 'TUNDRA',   ranges: [[54, 65]] },
    { id: 5,  name: 'FOREST',   ranges: [[66, 81], [142, 149]] },
    { id: 6,  name: 'SWAMP',    ranges: [[82, 93]] },
    { id: 7,  name: 'DIRT',     ranges: [[94, 105]] },
    { id: 8,  name: 'MUD',      ranges: [[106, 117]] },
    { id: 9,  name: 'VOLCANIC', ranges: [[118, 129]] },
    { id: 10, name: 'SNOW',     ranges: [[130, 141]] },
    { id: 11, name: 'DESERT',   ranges: [[150, 165]] },
];

export const NUM_TILE_CATEGORIES = TILE_CATEGORIES.length; // 12

/**
 * Generate a WGSL function `tileCategory(t: u32) -> u32` that maps
 * a tile type ID to its material category index.
 * Returns 255 for unmapped IDs.
 */
export function buildTileCategoryLookupWGSL() {
    const allRanges = [];
    for (const cat of TILE_CATEGORIES) {
        for (const [lo, hi] of cat.ranges) {
            allRanges.push({ lo, hi, id: cat.id, name: cat.name });
        }
    }
    allRanges.sort((a, b) => a.lo - b.lo);

    const lines = ['fn tileCategory(t: u32) -> u32 {'];
    for (const r of allRanges) {
        lines.push(
            `    if (t >= ${r.lo}u && t <= ${r.hi}u) { return ${r.id}u; } // ${r.name}`
        );
    }
    lines.push('    return 255u;');
    lines.push('}');
    return lines.join('\n');
}